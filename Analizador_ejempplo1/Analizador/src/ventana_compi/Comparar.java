public class Comparar {
    private String ruta1;
    private String ruta2;
    
    public Comparar(String ruta1, String ruta2){
        this.ruta1 = ruta1;
        this.ruta2 = ruta2;
    }
    
    public String getRuta1(){
        return this.ruta1;
    }
    
    public String getRuta2(){
        return this.ruta2;
    }
}
